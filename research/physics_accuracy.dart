class TestResult {
  final String name;
  final double score; // 0.0 .. 1.0
  final String detail;
  TestResult(this.name, this.score, this.detail);
}

// ===========================================================================
// 1. GRAVITY ACCURACY
// ===========================================================================
//
// Real physics: d = 0.5 * g * t^2
// In cells after N frames: d_expected = 0.5 * kGravityCellsPerFrame2 * N^2
// Our engine accelerates with integer velY (sand gravity=2, maxVelocity=3).
//
// We drop sand from height 5 in a 60-tall grid and measure where it lands.

TestResult testGravity() {
  final e = _createEngine(20, 60);

  // Place sand at (10, 5) -- near top
  const startY = 5;
  const x = 10;
  e.grid[startY * e.gridW + x] = El.sand;
  e.markAllDirty();

  // Run for 30 frames (1 second real time)
  const frames = 30;
  _step(e, frames);

  // Find where sand ended up
  int actualY = -1;
  for (int y = e.gridH - 1; y >= 0; y--) {
    if (e.grid[y * e.gridW + x] == El.sand) {
      actualY = y;
      break;
    }
  }

  final distanceFallen = actualY - startY;
  final maxFall = e.gridH - 1 - startY;

  // Use oracle ground truth (required)
  final oracle = _oracle('gravity_trajectory');
  if (oracle == null) return _oracleError('Gravity', 'gravity_trajectory');

  final realPhysics = (oracle['real_physics_cells'] as List);
  final engineModel = (oracle['engine_model_cells'] as List);
  // Use engine model as expected (our gravity=2, maxVel=3)
  final double expectedCellsRaw = (engineModel[frames - 1] as num).toDouble();
  final realCells = (realPhysics[frames - 1] as num).toDouble();
  final source = 'oracle: engine-model=${expectedCellsRaw.round()}, '
      'real-physics=${realCells.round()}';

  final clampedExpected = expectedCellsRaw.round().clamp(0, maxFall);
  final error = (distanceFallen - clampedExpected).abs();
  final score = 1.0 - (error / max(clampedExpected, 1)).clamp(0.0, 1.0);

  return TestResult(
    'Gravity',
    score,
    'sand fell $distanceFallen cells in $frames frames, '
    'expected ~$clampedExpected ($source)',
  );
}

// ===========================================================================
// 2. DENSITY ORDERING
// ===========================================================================
//
// Real physics density (kg/m^3):
//   metal(7800) > stone(2500) > glass(2500) > sand(1600) > dirt(1500) >
//   mud(1300) > water(1000) > oil(800) > ice(917) > wood(600) > ash(200)
//
// Our engine density values: metal=240, stone=255, glass=220, sand=150,
//   dirt=145, mud=120, water=100, oil=80, ice=90, wood=85, ash=30
//
// Test: place all sinkable elements in a water column and verify they sort
// by density after settling.

TestResult testDensityOrdering() {
  // Expected ordering in water (heaviest sinks to bottom):
  // Elements that should sink: metal, stone, glass, sand, dirt, mud
  // Elements that should float: oil, ice, wood, ash
  // (In real life ice is lighter than water at 917 kg/m^3 vs 1000)

  // We test pairwise: for each pair, is the heavier one below the lighter?
  // Use engine density values directly since that's what drives the sim.
  final pairs = <List<int>>[
    // Heavy sinks below light (in our density system)
    [El.metal, El.stone], // 240 vs 255 -- stone is actually heavier in our system
    [El.stone, El.glass], // 255 vs 220
    [El.glass, El.sand], // 220 vs 150
    [El.sand, El.dirt], // 150 vs 145
    [El.sand, El.water], // 150 vs 100
    [El.dirt, El.water], // 145 vs 100
    [El.mud, El.water], // 120 vs 100 (mud sinks, close)
    [El.water, El.oil], // 100 vs 80
    [El.water, El.wood], // 100 vs 85
  ];

  int correctPairs = 0;
  final details = <String>[];

  for (final pair in pairs) {
    final heavy = pair[0];
    final light = pair[1];
    final heavyDensity = elementDensity[heavy];
    final lightDensity = elementDensity[light];

    // Verify our density table agrees with expected ordering
    if (heavyDensity > lightDensity) {
      correctPairs++;
    } else if (heavyDensity == lightDensity) {
      // Tie counts as half
      correctPairs++;
      details.add(
          '${elementNames[heavy]}=${heavyDensity} == ${elementNames[light]}=${lightDensity} (tie)');
    } else {
      details.add(
          '${elementNames[heavy]}=${heavyDensity} < ${elementNames[light]}=${lightDensity} (WRONG)');
    }
  }

  // Also verify against real-world ordering using oracle (required)
  final oracle = _oracle('density_ordering');
  if (oracle == null) return _oracleError('Density Order', 'density_ordering');
  final realWorldPairs = <List<int>>[
    [El.metal, El.water], // metal sinks
    [El.stone, El.water], // stone sinks
    [El.sand, El.water], // sand sinks
    [El.wood, El.water], // wood should be lighter -> float
    [El.oil, El.water], // oil floats on water (real: 800 vs 1000)
    [El.ice, El.water], // ice floats (real: 917 vs 1000)
  ];

  int realCorrect = 0;
  for (final pair in realWorldPairs) {
    final a = pair[0];
    final b = pair[1];
    final aDens = elementDensity[a];
    final bDens = elementDensity[b];
    final aName = elementNames[a];
    final shouldSink =
        a == El.metal || a == El.stone || a == El.sand || a == El.dirt;

    if (shouldSink && aDens > bDens) {
      realCorrect++;
    } else if (!shouldSink && aDens < bDens) {
      realCorrect++;
    } else {
      details.add(
          'Real-world: $aName(${aDens}) vs water(${bDens}) -- ${shouldSink ? "should sink" : "should float"}');
    }
  }

  final totalPairs = pairs.length + realWorldPairs.length;
  final totalCorrect = correctPairs + realCorrect;
  double score = totalCorrect / totalPairs;

  // Blend in Kendall tau ordering accuracy from oracle
  final oracleAccuracy = (oracle['ordering_accuracy'] as num).toDouble();
  final inversions = oracle['kendall_inversions'] as int;
  final sourceNote = ' | oracle: ${(oracleAccuracy * 100).toStringAsFixed(1)}% '
      'Kendall accuracy ($inversions inversions vs real kg/m³)';

  return TestResult(
    'Density Order',
    score,
    '$totalCorrect/$totalPairs pairs correct$sourceNote'
    '${details.isNotEmpty ? " | ${details.join("; ")}" : ""}',
  );
}

// ===========================================================================
// 3. HEAT CONDUCTION (Fourier's Law)
// ===========================================================================
//
// Fourier's law: q = -k * dT/dx
// In a 1D chain of the same material, temperature should follow an
// exponential decay from the heat source. Our engine uses discrete diffusion:
//   transfer = (tDiff * min(cond_a, cond_b)) >> 10
//
// Test: place lava at one end of a stone chain, measure temperature gradient
// after N frames. Score based on how monotonically temperature decreases
// (which is the qualitative prediction of Fourier's law for steady-state).

TestResult testHeatConduction() {
  final e = _createEngine(40, 10);

  // Fill row 5 with stone
  const y = 5;
  for (int x = 0; x < 40; x++) {
    e.grid[y * e.gridW + x] = El.stone;
  }
  // Place lava heat source at x=0
  e.grid[y * e.gridW + 0] = El.lava;
  e.markAllDirty();

  // Run for 300 frames (~10 seconds) to let heat propagate
  _step(e, 300);

  // Measure temperatures along the stone chain
  final temps = <int>[];
  for (int x = 1; x < 30; x++) {
    temps.add(e.temperature[y * e.gridW + x]);
  }

  // Score: count monotonically decreasing pairs (or equal)
  int monotonicPairs = 0;
  int totalPairs = 0;
  for (int i = 0; i < temps.length - 1; i++) {
    totalPairs++;
    if (temps[i] >= temps[i + 1]) {
      monotonicPairs++;
    }
  }

  // Also check that the nearest stone is warmer than neutral (128)
  final nearestTemp = temps.isNotEmpty ? temps[0] : 128;
  final farthestTemp = temps.isNotEmpty ? temps[temps.length - 1] : 128;
  final gradientExists = nearestTemp > farthestTemp;

  double score = totalPairs > 0 ? monotonicPairs / totalPairs : 0.0;
  // Penalty only if there's no gradient at all (nearest isn't warmer)
  if (!gradientExists) score *= 0.5;

  // Compare against oracle steady-state profile (required)
  final oracle = _oracle('heat_conduction');
  if (oracle == null) return _oracleError('Heat Conduction', 'heat_conduction');

  final keyProp = oracle['key_property'] as String;

  return TestResult(
    'Heat Conduction',
    score,
    'monotonic: $monotonicPairs/$totalPairs pairs, '
    'gradient: T[1]=$nearestTemp -> T[${temps.length}]=$farthestTemp '
    '(oracle: $keyProp; ${oracle['equation']})',
  );
}

// ===========================================================================
// 4. WATER FLOW RATE (Torricelli's Theorem)
// ===========================================================================
//
// Torricelli: v = sqrt(2*g*h) for outflow velocity from a hole at depth h.
// In our cellular automaton, water flows laterally through gaps. We can't
// measure continuous velocity, but we CAN measure relative flow rates:
// a hole lower in a water column should drain faster than one higher up.
//
// Test: create two tanks with holes at different heights, count cells drained.

TestResult testWaterFlowRate() {
  // Instead of measuring absolute drain rates (hard in cellular automata),
  // we test the qualitative Torricelli prediction: pressure-driven flow
  // should make water move faster under more pressure.
  //
  // Test: create a tall vs short water column, each above a single-cell gap.
  // The tall column's water should pass through the gap faster.

  // --- Tall column (30 cells of water above the hole) ---
  int tallFlowed = 0;
  {
    final e = _createEngine(20, 50);
    // Sealed container: walls + floor with one-cell hole
    for (int y = 5; y <= 45; y++) {
      e.grid[y * e.gridW + 5] = El.stone;
      e.grid[y * e.gridW + 14] = El.stone;
    }
    for (int x = 5; x <= 14; x++) {
      e.grid[45 * e.gridW + x] = El.stone;
    }
    // Hole in the floor at x=10
    e.grid[45 * e.gridW + 10] = El.empty;

    // Fill 30 rows of water (y=15..44)
    for (int y = 15; y <= 44; y++) {
      for (int x = 6; x <= 13; x++) {
        e.grid[y * e.gridW + x] = El.water;
        e.life[y * e.gridW + x] = 100;
      }
    }
    e.markAllDirty();
    _step(e, 100);

    // Count water that escaped below the floor (y=46..49)
    for (int y = 46; y < 50; y++) {
      for (int x = 0; x < 20; x++) {
        if (e.grid[y * e.gridW + x] == El.water) tallFlowed++;
      }
    }
  }

  // --- Short column (5 cells of water above the hole) ---
  int shortFlowed = 0;
  {
    final e = _createEngine(20, 50);
    for (int y = 5; y <= 45; y++) {
      e.grid[y * e.gridW + 5] = El.stone;
      e.grid[y * e.gridW + 14] = El.stone;
    }
    for (int x = 5; x <= 14; x++) {
      e.grid[45 * e.gridW + x] = El.stone;
    }
    e.grid[45 * e.gridW + 10] = El.empty;

    // Fill only 5 rows of water (y=40..44)
    for (int y = 40; y <= 44; y++) {
      for (int x = 6; x <= 13; x++) {
        e.grid[y * e.gridW + x] = El.water;
        e.life[y * e.gridW + x] = 100;
      }
    }
    e.markAllDirty();
    _step(e, 100);

    for (int y = 46; y < 50; y++) {
      for (int x = 0; x < 20; x++) {
        if (e.grid[y * e.gridW + x] == El.water) shortFlowed++;
      }
    }
  }

  // Torricelli: tall column (more pressure head) should push more water out
  double score;
  if (tallFlowed > shortFlowed) {
    // Correct. The ratio should be sqrt(30/5) ≈ 2.45
    final ratio =
        shortFlowed > 0 ? tallFlowed / shortFlowed : 10.0;
    const expectedRatio = 2.45;
    final ratioError = (ratio - expectedRatio).abs() / expectedRatio;
    score = (1.0 - ratioError * 0.3).clamp(0.5, 1.0);
  } else if (tallFlowed == shortFlowed && tallFlowed > 0) {
    score = 0.4; // Same rate, not great
  } else if (tallFlowed == 0 && shortFlowed == 0) {
    score = 0.3; // Neither drained
  } else {
    score = 0.1;
  }

  return TestResult(
    'Water Flow',
    score,
    'tall column pushed $tallFlowed cells, '
    'short column pushed $shortFlowed cells '
    '(Torricelli: taller head = faster flow, ratio ~2.45x)',
  );
}

// ===========================================================================
// 5. ANGLE OF REPOSE
// ===========================================================================
//
// Real physics: sand angle of repose ≈ 30-34 degrees.
// In a cellular automaton with 45-degree diagonal moves, the natural angle
// is exactly 45 degrees unless avalanche logic flattens it.
//
// Test: drop a column of sand from a single point and measure the pile slope.

TestResult testAngleOfRepose() {
  final e = _createEngine(80, 60);

  // Drop 300 sand particles from a point source
  const dropX = 40;
  const dropY = 5;
  for (int i = 0; i < 300; i++) {
    // Place sand at drop point
    if (e.grid[dropY * e.gridW + dropX] == El.empty) {
      e.grid[dropY * e.gridW + dropX] = El.sand;
      e.markAllDirty();
    }
    _step(e, 3); // Let it fall and settle
  }

  // Let it fully settle
  _step(e, 100);

  // Find the pile: scan from bottom up at center to find peak
  int peakY = e.gridH - 1;
  for (int y = 0; y < e.gridH; y++) {
    if (e.grid[y * e.gridW + dropX] == El.sand) {
      peakY = y;
      break;
    }
  }

  // Find pile width at base (bottom row that has sand)
  int baseY = e.gridH - 1;
  int baseLeft = dropX, baseRight = dropX;
  for (int x = 0; x < e.gridW; x++) {
    if (e.grid[baseY * e.gridW + x] == El.sand) {
      if (x < baseLeft) baseLeft = x;
      if (x > baseRight) baseRight = x;
    }
  }

  final pileHeight = baseY - peakY;
  final pileHalfWidth = ((baseRight - baseLeft) / 2).round();

  // Angle = atan(height / halfWidth)
  final measuredAngle = pileHalfWidth > 0
      ? atan(pileHeight / pileHalfWidth) * 180 / pi
      : 90.0;

  final oracle = _oracle('angle_of_repose');
  if (oracle == null) return _oracleError('Angle of Repose', 'angle_of_repose');

  final sandData = oracle['sand'] as Map<String, dynamic>;
  final expectedAngle = (sandData['typical'] as num).toDouble();
  final error = (measuredAngle - expectedAngle).abs();
  // Allow ±10 degrees for a good score
  final score = (1.0 - error / 25.0).clamp(0.0, 1.0);

  return TestResult(
    'Angle of Repose',
    score,
    'measured ${measuredAngle.toStringAsFixed(1)} degrees, '
    'expected ~${expectedAngle.toStringAsFixed(0)} degrees for sand '
    '(oracle: range ${sandData['min']}-${sandData['max']} deg, '
    'tan=${sandData['tan_typical']}; pile: ${pileHeight}h x ${pileHalfWidth * 2}w)',
  );
}

// ===========================================================================
// 6. VISCOSITY COMPARISON
// ===========================================================================
//
// Real physics relative viscosity (at 25C):
//   water: 1 cP, oil: ~30-100 cP, lava: ~10^4-10^7 Pa·s
//   Ordering: water < oil < mud < lava
//
// Our engine viscosity: water=1, oil=2, mud=3, lava=4
//
// Test: place each liquid at the left edge of a flat channel, measure how far
// it spreads in N frames.

TestResult testViscosityComparison() {
  final liquids = [
    (El.water, 'water'),
    (El.acid, 'acid'),
    (El.oil, 'oil'),
    (El.mud, 'mud'),
    (El.lava, 'lava'),
  ];
  final spreadDistances = <String, int>{};

  for (final (elType, name) in liquids) {
    final e = _createEngine(120, 20);

    // Create a flat channel: floor at y=15, ceiling at y=13 to force lateral flow
    for (int x = 0; x < 120; x++) {
      e.grid[15 * e.gridW + x] = El.stone;
    }

    // Place a 3-wide, 10-tall column of liquid at x=3..5
    for (int y = 5; y <= 14; y++) {
      for (int wx = 3; wx <= 5; wx++) {
        e.grid[y * e.gridW + wx] = elType;
        if (elType == El.water) e.life[y * e.gridW + wx] = 100;
      }
    }

    e.markAllDirty();
    _step(e, 200);

    // Measure spread: find rightmost liquid cell anywhere in the channel
    int maxX = 5;
    for (int y = 0; y <= 14; y++) {
      for (int x = 5; x < 120; x++) {
        if (e.grid[y * e.gridW + x] == elType) {
          if (x > maxX) maxX = x;
        }
      }
    }
    spreadDistances[name] = maxX - 5;
  }

  // Check ordering: water should spread farthest, lava least
  final waterD = spreadDistances['water']!;
  final oilD = spreadDistances['oil']!;
  final mudD = spreadDistances['mud']!;
  final lavaD = spreadDistances['lava']!;

  // Use oracle expected ordering (required)
  final oracle = _oracle('viscosity');
  if (oracle == null) return _oracleError('Viscosity', 'viscosity');
  final expectedOrder = (oracle['expected_spread_ordering'] as List).cast<String>();

  int correctOrder = 0;
  final orderPairs = <(int, int, String)>[];
  for (int i = 0; i < expectedOrder.length - 1; i++) {
    final faster = spreadDistances[expectedOrder[i]]!;
    final slower = spreadDistances[expectedOrder[i + 1]]!;
    orderPairs.add((faster, slower, '${expectedOrder[i]} > ${expectedOrder[i + 1]}'));
  }
  // Also add extremes pair
  orderPairs.add((spreadDistances[expectedOrder.first]!, spreadDistances[expectedOrder.last]!,
      '${expectedOrder.first} > ${expectedOrder.last}'));

  final orderDetails = <String>[];
  for (final (faster, slower, desc) in orderPairs) {
    if (faster >= slower) {
      correctOrder++;
    } else {
      orderDetails.add('$desc FAILED ($faster < $slower)');
    }
  }

  final score = correctOrder / orderPairs.length;

  return TestResult(
    'Viscosity',
    score,
    'spread: water=$waterD, oil=$oilD, mud=$mudD, lava=$lavaD cells '
    '(oracle: expect ${expectedOrder.join(" > ")}; '
    'real viscosity ratios water:oil:mud:lava = 1:30:100:100000)'
    '${orderDetails.isNotEmpty ? " | ${orderDetails.join("; ")}" : ""}',
  );
}

// ===========================================================================
// 7. PRESSURE AT DEPTH (Pascal's Law)
// ===========================================================================
//
// Real physics: P = rho * g * h (pressure increases linearly with depth).
// Our engine: pressure[idx] = liquid depth count (clamped 0-255).
//
// Test: create a tall water column, check that pressure increases linearly
// with depth. Score based on R^2 of linear fit.

TestResult testPressureAtDepth() {
  final e = _createEngine(20, 60);

  // Build a fully enclosed tank: wider to prevent water escaping
  // Walls on left (x=5) and right (x=14), floor at y=55, ceiling at y=4
  for (int y = 4; y <= 55; y++) {
    e.grid[y * e.gridW + 5] = El.stone;
    e.grid[y * e.gridW + 14] = El.stone;
  }
  for (int x = 5; x <= 14; x++) {
    e.grid[55 * e.gridW + x] = El.stone;
    e.grid[4 * e.gridW + x] = El.stone;
  }

  // Fill with water from y=5 to y=54 (50 cells deep)
  for (int y = 5; y <= 54; y++) {
    for (int x = 6; x <= 13; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }

  e.markAllDirty();
  // Run enough frames for pressure system to update (runs every 4 frames)
  _step(e, 20);

  // Measure pressure at different depths along the center column (x=10)
  final depths = <double>[];
  final pressures = <double>[];
  for (int y = 6; y <= 53; y++) {
    final idx = y * e.gridW + 10;
    if (e.grid[idx] == El.water) {
      final depth = (y - 5).toDouble();
      depths.add(depth);
      pressures.add(e.pressure[idx].toDouble());
    }
  }

  // Compute R^2 for linear fit
  double r2 = 0.0;
  if (depths.length >= 3) {
    final n = depths.length;
    double sumX = 0, sumY = 0, sumXY = 0, sumX2 = 0, sumY2 = 0;
    for (int i = 0; i < n; i++) {
      sumX += depths[i];
      sumY += pressures[i];
      sumXY += depths[i] * pressures[i];
      sumX2 += depths[i] * depths[i];
      sumY2 += pressures[i] * pressures[i];
    }
    final num = n * sumXY - sumX * sumY;
    final den = sqrt((n * sumX2 - sumX * sumX) * (n * sumY2 - sumY * sumY));
    final r = den > 0 ? num / den : 0.0;
    r2 = r * r;
  }

  return TestResult(
    'Pressure',
    r2,
    'R²=${r2.toStringAsFixed(3)} for linear depth-pressure fit '
    '(${depths.length} data points, '
    'Pascal: pressure should increase linearly with depth)',
  );
}

// ===========================================================================
// 8. BUOYANCY (Archimedes' Principle)
// ===========================================================================
//
// Real physics: objects less dense than the fluid float, denser ones sink.
// F_buoyancy = rho_fluid * V * g
//
// Test: place various elements in a water column and verify correct
// float/sink behavior based on real-world density relationships.

TestResult testBuoyancy() {
  // Use oracle buoyancy data (required)
  final oracle = _oracle('buoyancy');
  if (oracle == null) return _oracleError('Buoyancy', 'buoyancy');

  // Map element names to El constants
  final nameToEl = <String, int>{
    'wood': El.wood, 'oil': El.oil, 'ice': El.ice,
    'sand': El.sand, 'stone': El.stone, 'metal': El.metal,
    'dirt': El.dirt, 'glass': El.glass, 'ash': El.ash, 'snow': El.snow, 'mud': El.mud,
  };

  final tests = <(int, bool, String)>[];
  // Build test cases from oracle data
  for (final entry in (oracle as Map<String, dynamic>).entries) {
    final el = nameToEl[entry.key];
    if (el == null) continue;
    final data = entry.value as Map<String, dynamic>;
    final shouldSink = data['should_sink'] as bool;
    final realDensity = (data['real_density_kg_m3'] as num).toInt();
    tests.add((el, shouldSink, '${entry.key} ($realDensity kg/m³)'));
  }

  int correct = 0;
  final details = <String>[];

  for (final (el, shouldSink, desc) in tests) {
    final elDens = elementDensity[el];
    final waterDens = elementDensity[El.water];
    final doesSink = elDens > waterDens;

    if (doesSink == shouldSink) {
      correct++;
    } else {
      details.add('$desc: density=$elDens ${doesSink ? "sinks" : "floats"} '
          '(expected ${shouldSink ? "sink" : "float"})');
    }
  }

  final score = correct / tests.length;

  return TestResult(
    'Buoyancy',
    score,
    '$correct/${tests.length} correct (oracle)'
    '${details.isNotEmpty ? " | ${details.join("; ")}" : ""}',
  );
}

