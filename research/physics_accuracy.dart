// 28. DOMINO CASCADE (Sequential Collapse)
// ===========================================================================
//
// Real physics: removing a support should trigger sequential collapse,
// not instant. Test that unsupported elements fall progressively.

TestResult testDominoCascade() {
  // Test: a tall column of sand sitting on a narrow ledge. Remove the ledge
  // and measure how long the sand takes to fall and settle. It should take
  // multiple frames (gravity is finite, not teleportation).
  final e = _createEngine(40, 50);

  // Stone floor
  for (int x = 0; x < 40; x++) {
    e.grid[45 * e.gridW + x] = El.stone;
  }

  // Narrow stone shelf at y=25 (x=15..25)
  for (int x = 15; x <= 25; x++) {
    e.grid[25 * e.gridW + x] = El.stone;
  }

  // Sand piled on shelf (y=10..24, x=16..24)
  for (int y = 10; y <= 24; y++) {
    for (int x = 16; x <= 24; x++) {
      e.grid[y * e.gridW + x] = El.sand;
    }
  }

  e.markAllDirty();
  _step(e, 10); // Let initial physics settle

  // Remove the shelf
  for (int x = 15; x <= 25; x++) {
    e.grid[25 * e.gridW + x] = El.empty;
  }
  e.markAllDirty();

  // Track collapse: count frames until all sand is below y=40
  int collapseFrames = 0;
  bool collapsed = false;
  for (int frame = 0; frame < 60; frame++) {
    e.markAllDirty();
    e.step(_simulateElement);
    collapseFrames = frame + 1;

    bool anyHigh = false;
    for (int y = 0; y <= 35; y++) {
      for (int x = 10; x <= 30; x++) {
        if (e.grid[y * e.gridW + x] == El.sand) {
          anyHigh = true;
          break;
        }
      }
      if (anyHigh) break;
    }
    if (!anyHigh) {
      collapsed = true;
      break;
    }
  }

  final oracle = _oracle('domino_cascade');
  if (oracle == null) return _oracleError('Domino Cascade', 'domino_cascade');

  final expectedMin = (oracle['expected_min_frames'] as num).toInt();
  final expectedMax = (oracle['expected_max_frames'] as num).toInt();

  // Score: should take multiple frames (not 1, not 60+)
  double score;
  if (collapsed && collapseFrames >= expectedMin && collapseFrames <= expectedMax) {
    score = 1.0;
  } else if (collapsed && collapseFrames < expectedMin) {
    score = 0.6;
  } else if (collapsed) {
    score = 0.7;
  } else {
    score = 0.3;
  }

  return TestResult(
    'Domino Cascade',
    score,
    'collapse took $collapseFrames frames '
    '${collapsed ? "(completed)" : "(incomplete)"} '
    '(oracle: expect $expectedMin-$expectedMax frames)',
  );
}

