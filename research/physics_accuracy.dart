// =============================================================================
// Physics Accuracy Benchmark
// =============================================================================
//
// Measures how closely The Particle Engine's cellular automaton approximates
// real-world physics. Each test sets up a scenario on a headless simulation
// grid, runs it for a controlled number of frames, then compares measured
// results against values derived from real physics equations.
//
// Usage:
//   python research/physics_oracle.py && dart run research/physics_accuracy.dart
//
// REQUIRES ground_truth.json generated by physics_oracle.py. All tests use
// scipy/numpy-derived values as the authoritative reference. No fallback
// constants -- the benchmark fails immediately if the oracle is missing.
// =============================================================================

