  for (int y = 4; y <= 16; y++) {
    for (int x = 4; x <= 9; x++) {
      e.grid[y * e.gridW + x] = El.acid;
    }
  }

  e.markAllDirty();

  for (int frame = 0; frame < 800; frame++) {
    e.markAllDirty();
