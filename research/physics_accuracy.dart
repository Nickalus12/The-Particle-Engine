// 29. ACID DISSOLUTION RATE (Surface Area)
// ===========================================================================
//
// Real physics: dissolution rate is proportional to surface area.
// A thin wall should dissolve faster than a thick wall.

TestResult testAcidDissolutionRate() {
  // Thin wall (1 cell thick)
  int thinTime = _measureDissolutionTime(1);
  // Thick wall (3 cells thick)
  int thickTime = _measureDissolutionTime(3);

  final oracle = _oracle('acid_dissolution');
  if (oracle == null) return _oracleError('Acid Dissolution', 'acid_dissolution');

  final expectedRatio = (oracle['expected_ratio_3x_to_1x'] as num).toDouble();

  // Thicker wall should take longer to dissolve
  double score;
  if (thickTime > thinTime && thinTime > 0) {
    final ratio = thickTime / thinTime;
    // Expected: roughly proportional to thickness (3x)
    score = (ratio / expectedRatio).clamp(0.3, 1.0);
  } else if (thinTime == 0 && thickTime == 0) {
    score = 0.3; // Neither dissolved
  } else if (thickTime == thinTime) {
    score = 0.4;
  } else {
    score = 0.2;
  }

  return TestResult(
    'Acid Dissolution',
    score,
    'thin wall: $thinTime frames, thick wall: $thickTime frames '
    '(oracle: expect ratio ~${expectedRatio.toStringAsFixed(1)}x; ${oracle['equation']})',
  );
}

int _measureDissolutionTime(int thickness) {
  final e = _createEngine(30, 20);

  // Container walls to keep acid pooled against the stone wall
  for (int y = 3; y <= 17; y++) {
    e.grid[y * e.gridW + 3] = El.metal; // left wall (acid-resistant)
  }
  for (int x = 3; x <= 15; x++) {
    e.grid[17 * e.gridW + x] = El.metal; // floor
  }

  // Stone wall at x=10..10+thickness
  for (int y = 4; y <= 16; y++) {
    for (int t = 0; t < thickness; t++) {
      e.grid[y * e.gridW + (10 + t)] = El.stone;
    }
  }

  // Pool acid against the wall (x=4..9)
  for (int y = 4; y <= 16; y++) {
    for (int x = 4; x <= 9; x++) {
      e.grid[y * e.gridW + x] = El.acid;
    }
  }

  e.markAllDirty();

  for (int frame = 0; frame < 800; frame++) {
    e.markAllDirty();
    e.step(_simulateElement);

    // Check if wall is breached at any row
    bool breached = false;
    for (int y = 5; y <= 15; y++) {
      bool rowClear = true;
      for (int t = 0; t < thickness; t++) {
        if (e.grid[y * e.gridW + (10 + t)] == El.stone) {
          rowClear = false;
          break;
        }
      }
      if (rowClear) {
        breached = true;
        break;
      }
    }
    if (breached) return frame + 1;
  }
  return 800; // Did not breach
}

