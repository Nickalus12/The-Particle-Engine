/// Ground truth data from the Python physics oracle, or null if unavailable.
Map<String, dynamic>? _groundTruth;

/// Try to load ground truth from research/ground_truth.json.
/// Returns true if loaded successfully.
bool _loadGroundTruth() {
  try {
    final file = File('research/ground_truth.json');
    if (!file.existsSync()) return false;
    _groundTruth = jsonDecode(file.readAsStringSync()) as Map<String, dynamic>;
    return true;
  } catch (e) {
    print('  Warning: Could not load ground_truth.json: $e');
    return false;
  }
}

/// Get a ground truth section, or null if not available.
Map<String, dynamic>? _oracle(String key) => _groundTruth?[key] as Map<String, dynamic>?;

/// Require a ground truth section. Returns error TestResult if missing.
/// Usage: final data = _requireOracle('key');
///        if (data == null) return _oracleError('Test Name', 'key');
TestResult _oracleError(String testName, String key) => TestResult(
    testName, 0.0,
    'ERROR: ground_truth.json missing key "$key". '
    'Run: python research/physics_oracle.py');

// ---------------------------------------------------------------------------
// Constants derived from our engine's scale
// ---------------------------------------------------------------------------

/// Our simulation runs at 30 fps (typical mobile target).
const double kFps = 30.0;
const double kDt = 1.0 / kFps;

/// Each grid cell represents ~1 cm in the real world (order-of-magnitude).
/// This is a design choice: a 400-wide grid = ~4 m scene, which feels right
/// for the sandbox viewport.
const double kCellSize = 0.01; // metres per cell

/// Real gravitational acceleration (m/s^2).
const double kRealG = 9.81;

/// Gravity in cells/frame^2, converting from m/s^2.
/// d = 0.5 * g * t^2 in metres  -->  cells = d / cellSize
/// Per-frame acceleration: g_cells = kRealG / kCellSize / kFps^2
/// = 9.81 / 0.01 / 900 ≈ 1.09 cells/frame^2
/// Our engine uses integer gravity values (sand=2, water=1), so there's a
/// built-in quantization error we measure.
const double kGravityCellsPerFrame2 = kRealG / kCellSize / (kFps * kFps);

