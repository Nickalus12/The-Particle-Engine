// temperatures) should remain roughly constant in an isolated system.

TestResult testEnergyConservation() {
  final e = _createEngine(40, 40);

  // Sealed stone box filled with stone (good conductor, no reactions)
  for (int x = 5; x <= 35; x++) {
    e.grid[5 * e.gridW + x] = El.stone;
    e.grid[35 * e.gridW + x] = El.stone;
  }
  for (int y = 5; y <= 35; y++) {
    e.grid[y * e.gridW + 5] = El.stone;
    e.grid[y * e.gridW + 35] = El.stone;
  }
  // Fill interior with stone
  for (int y = 6; y <= 34; y++) {
    for (int x = 6; x <= 34; x++) {
      e.grid[y * e.gridW + x] = El.stone;
    }
  }

  // Create a temperature gradient: left half hot, right half cold
  for (int y = 6; y <= 34; y++) {
    for (int x = 6; x <= 20; x++) {
      e.temperature[y * e.gridW + x] = 200;
    }
    for (int x = 21; x <= 34; x++) {
      e.temperature[y * e.gridW + x] = 56; // 200 and 56 average to 128
    }
  }
  e.markAllDirty();

  // Measure initial total temperature (interior only)
  int initialTotal = 0;
  for (int y = 6; y <= 34; y++) {
    for (int x = 6; x <= 34; x++) {
      initialTotal += e.temperature[y * e.gridW + x];
    }
  }

  _step(e, 300);

  int finalTotal = 0;
  for (int y = 6; y <= 34; y++) {
    for (int x = 6; x <= 34; x++) {
      finalTotal += e.temperature[y * e.gridW + x];
    }
  }

  final oracle = _oracle('conservation_energy');
  if (oracle == null) return _oracleError('Energy Conservation', 'conservation_energy');

  final drift = (initialTotal - finalTotal).abs();
  final tolerancePct = (oracle['tolerance_percent'] as num).toDouble();
  final score = initialTotal > 0
      ? (1.0 - drift / initialTotal).clamp(0.0, 1.0)
      : 1.0;

  return TestResult(
    'Energy Conservation',
    score,
    'initial total temp=$initialTotal, final=$finalTotal, '
    'drift=${(drift * 100.0 / initialTotal).toStringAsFixed(1)}% '
    '(oracle: expect ${oracle['expected_drift_percent']}% drift, tolerance ${tolerancePct}%)',
  );
}

// ===========================================================================
// 15. MOMENTUM CONSERVATION
// ===========================================================================
//
// In a closed system, total momentum (sum of velX, velY) should be roughly
// conserved. We test by dropping sand in a symmetric pattern and checking
// that net horizontal momentum stays near zero.

TestResult testMomentumConservation() {
  final e = _createEngine(40, 40);

  // Floor
  for (int x = 0; x < 40; x++) {
    e.grid[38 * e.gridW + x] = El.stone;
  }

  // Drop sand symmetrically: equal amounts at x=10 and x=30
  for (int y = 2; y <= 6; y++) {
    e.grid[y * e.gridW + 10] = El.sand;
    e.grid[y * e.gridW + 30] = El.sand;
  }

  e.markAllDirty();
  _step(e, 100);

  // Measure net horizontal momentum
  int totalVelX = 0;
  int totalVelY = 0;
  int count = 0;
  for (int y = 0; y < 40; y++) {
    for (int x = 0; x < 40; x++) {
      final idx = y * e.gridW + x;
      if (e.grid[idx] != El.empty && e.grid[idx] != El.stone) {
        totalVelX += e.velX[idx];
        totalVelY += e.velY[idx];
        count++;
      }
    }
  }

  final oracle = _oracle('conservation_momentum');
  if (oracle == null) return _oracleError('Momentum Conservation', 'conservation_momentum');

  // Symmetric drop -> net velX should be ~0
  final expectedNet = (oracle['expected_net_horizontal_momentum'] as num).toInt();
  final netMomentum = totalVelX.abs();
  // Score: 0 net momentum = perfect
  final score = count > 0
      ? (1.0 - netMomentum / max(count, 1)).clamp(0.0, 1.0)
      : 1.0;

  return TestResult(
    'Momentum Conservation',
    score,
    'net velX=$totalVelX, net velY=$totalVelY, particles=$count '
    '(oracle: expect net horizontal momentum ~$expectedNet)',
  );
}

// ===========================================================================
// 16. RIPPLE DAMPING
// ===========================================================================
//
// After disturbing a calm water surface, the disturbance should decay over
// time (not amplify). Real water waves dissipate energy through viscosity.

TestResult testRippleDamping() {
  final e = _createEngine(60, 30);

  // Floor
  for (int x = 0; x < 60; x++) {
    e.grid[25 * e.gridW + x] = El.stone;
  }
  // Fill water (y=15..24)
  for (int y = 15; y <= 24; y++) {
    for (int x = 0; x < 60; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }
  e.markAllDirty();
  // Let it settle
  _step(e, 50);

  // Measure calm surface variance
  final surfaceHeights1 = <int>[];
  for (int x = 0; x < 60; x++) {
    for (int y = 0; y < 30; y++) {
      if (e.grid[y * e.gridW + x] == El.water) {
        surfaceHeights1.add(y);
        break;
      }
    }
  }

  // Disturb: drop a block of water at center
  for (int y = 5; y <= 10; y++) {
    for (int x = 28; x <= 32; x++) {
      if (e.grid[y * e.gridW + x] == El.empty) {
        e.grid[y * e.gridW + x] = El.water;
        e.life[y * e.gridW + x] = 100;
      }
    }
  }
  e.markAllDirty();

  // Measure right after disturbance settles partially
  _step(e, 30);
  double earlyVariance = _surfaceVariance(e, 60);

  // Let it settle more
  _step(e, 200);
  double lateVariance = _surfaceVariance(e, 60);

  final oracle = _oracle('ripple_damping');
  if (oracle == null) return _oracleError('Ripple Damping', 'ripple_damping');

  // Damping: late variance should be less than early variance
  double score;
  if (earlyVariance > 0 && lateVariance <= earlyVariance) {
    final dampingRatio = 1.0 - lateVariance / earlyVariance;
    score = (0.5 + dampingRatio * 0.5).clamp(0.0, 1.0);
  } else if (earlyVariance == 0 && lateVariance == 0) {
    score = 1.0; // Perfectly calm
  } else {
    score = 0.2; // Amplifying -- bad
  }

  return TestResult(
    'Ripple Damping',
    score,
    'early variance=${earlyVariance.toStringAsFixed(2)}, '
    'late variance=${lateVariance.toStringAsFixed(2)} '
    '(oracle: ${oracle['mechanism']})',
  );
}

double _surfaceVariance(SimulationEngine e, int width) {
  final heights = <int>[];
  for (int x = 0; x < width; x++) {
    for (int y = 0; y < e.gridH; y++) {
      if (e.grid[y * e.gridW + x] == El.water) {
        heights.add(y);
        break;
      }
    }
  }
  if (heights.length < 2) return 0.0;
  final mean = heights.reduce((a, b) => a + b) / heights.length;
  double variance = 0;
  for (final h in heights) {
    variance += (h - mean) * (h - mean);
  }
  return variance / heights.length;
}

// ===========================================================================
// 17. HOURGLASS FLOW (Beverloo Equation)
// ===========================================================================
//
// Real physics: flow rate through a narrow opening is proportional to
// (D - k*d)^(5/2) where D = opening width, d = particle diameter.
// For our grid, d=1 cell. Key prediction: wider opening = faster flow.

TestResult testHourglassFlow() {
  // Test two opening widths: 1 cell vs 3 cells
  int flow1 = _measureHourglassFlow(1);
  int flow3 = _measureHourglassFlow(3);

  // Use oracle Beverloo data (required)
  final oracle = _oracle('beverloo');
  if (oracle == null) return _oracleError('Hourglass Flow', 'beverloo');
  final expectedFlows = (oracle['expected_relative_flow'] as List);
  // Index 0 = 1-cell opening, index 2 = 3-cell opening
  final flow1Expected = (expectedFlows[0] as num).toDouble();
  final flow3Expected = (expectedFlows[2] as num).toDouble();
  final expectedRatio = flow3Expected > 0 && flow1Expected > 0
      ? flow3Expected / flow1Expected
      : 15.6;
  const beverlooNote = ' | oracle: Beverloo Q=C*sqrt(g)*(D-k*d)^2.5';

  double score;
  if (flow3 > flow1 && flow1 > 0) {
    final ratio = flow3 / flow1;
    // Beverloo predicts ratio ~15.6 for 3-cell vs 1-cell (D^2.5).
    // But our CA will never match exactly. Score tiers:
    // ratio >= 5.0 → 100% (excellent CA approximation)
    // ratio >= 3.0 → 80%
    // ratio >= 2.0 → 60%
    // ratio >= 1.5 → 40%
    // ratio > 1.0  → 20% (at least qualitatively correct)
    if (ratio >= 5.0) {
      score = 1.0;
    } else if (ratio >= 3.0) {
      score = 0.8;
    } else if (ratio >= 2.0) {
      score = 0.6;
    } else if (ratio >= 1.5) {
      score = 0.4;
    } else {
      score = 0.2;
    }
  } else if (flow3 > 0 && flow1 == 0) {
    // 1-cell jammed but 3-cell flowed — qualitatively correct
    score = 0.7;
  } else if (flow3 == flow1 && flow3 > 0) {
    // Same rate regardless of opening — wrong
    score = 0.1;
  } else {
    score = 0.0;
  }

  return TestResult(
    'Hourglass Flow',
    score,
    '1-cell opening: $flow1 grains, 3-cell opening: $flow3 grains drained '
    '(Beverloo: expected ratio ~${expectedRatio.toStringAsFixed(1)}x)$beverlooNote',
  );
}

int _measureHourglassFlow(int openingWidth) {
  // Use a TALL, WIDE hourglass with lots of sand so it doesn't fully drain
  // in the measurement window. We measure how many grains pass through
  // in a SHORT time window to capture flow RATE, not completion.
  final e = _createEngine(40, 80);

  // Build hourglass: top chamber, narrow neck, bottom chamber
  // Walls
  for (int y = 5; y <= 75; y++) {
    e.grid[y * e.gridW + 5] = El.stone;
    e.grid[y * e.gridW + 35] = El.stone;
  }
  // Neck at y=40: stone wall with opening in the center
  for (int x = 5; x <= 35; x++) {
    e.grid[40 * e.gridW + x] = El.stone;
  }
  // Opening: centered at x=20
  final center = 20 - openingWidth ~/ 2;
  for (int ox = center; ox < center + openingWidth; ox++) {
    e.grid[40 * e.gridW + ox] = El.empty;
  }
  // Floor under bottom chamber
  for (int x = 5; x <= 35; x++) {
    e.grid[75 * e.gridW + x] = El.stone;
  }

  // Fill top chamber DENSELY with sand (y=6..39, x=6..34)
  for (int y = 6; y <= 39; y++) {
    for (int x = 6; x <= 34; x++) {
      e.grid[y * e.gridW + x] = El.sand;
    }
  }

  e.markAllDirty();
  // Run only 30 frames — short enough that sand won't fully drain
  // even through a 3-cell opening. This measures RATE.
  _step(e, 30);

  // Count sand that reached bottom chamber (y=41..74)
  int bottomSand = 0;
  for (int y = 41; y <= 74; y++) {
    for (int x = 6; x <= 34; x++) {
      if (e.grid[y * e.gridW + x] == El.sand) bottomSand++;
    }
  }

  return bottomSand;
}

// ===========================================================================
// 18. JAMMING TRANSITION
// ===========================================================================
//
// Real physics: granular materials can form arches over narrow openings,
// causing intermittent flow. With a 1-cell opening, jamming should be
// more frequent than with wider openings.

TestResult testJammingTransition() {
  // Run multiple trials with 1-cell opening, count how many jam
  int jamCount = 0;
  const trials = 5;

  for (int trial = 0; trial < trials; trial++) {
    final e = _createEngine(20, 40);

    // Walls
    for (int y = 5; y <= 35; y++) {
      e.grid[y * e.gridW + 3] = El.stone;
      e.grid[y * e.gridW + 17] = El.stone;
    }
    // Barrier with 1-cell opening
    for (int x = 3; x <= 17; x++) {
      e.grid[20 * e.gridW + x] = El.stone;
    }
    e.grid[20 * e.gridW + 10] = El.empty; // 1-cell opening

    // Fill top with sand
    for (int y = 6; y <= 19; y++) {
      for (int x = 4; x <= 16; x++) {
        e.grid[y * e.gridW + x] = El.sand;
      }
    }

    e.markAllDirty();
    _step(e, 300);

    // Check if sand remains above the opening (jammed)
    int sandAbove = 0;
    for (int y = 6; y <= 19; y++) {
      for (int x = 4; x <= 16; x++) {
        if (e.grid[y * e.gridW + x] == El.sand) sandAbove++;
      }
    }
    // If more than 20% of sand is still above, consider it jammed
    if (sandAbove > 26) jamCount++;
  }

  final oracle = _oracle('jamming_transition');
  if (oracle == null) return _oracleError('Jamming Transition', 'jamming_transition');

  // Real sand DOES jam with 1-cell openings. We expect at least some trials
  // to show jamming. If 0 jam, that's less realistic (but not terrible).
  double score;
  if (jamCount > 0 && jamCount < trials) {
    score = 1.0; // Some jam, some don't -- realistic
  } else if (jamCount == trials) {
    score = 0.7; // Always jams -- plausible but not ideal
  } else {
    score = 0.5; // Never jams -- less realistic
  }

  return TestResult(
    'Jamming Transition',
    score,
    '$jamCount/$trials trials jammed with 1-cell opening '
    '(oracle: expected jam probability ~${oracle['expected_jam_probability_1cell']})',
  );
}

// ===========================================================================
// 19. SEDIMENT GRADED BEDDING
// ===========================================================================
//
// Real physics: in still water, heavier/denser particles settle first
// (graded bedding). Sand (density 150) should settle below dirt (145)
// in a water column -- though the difference is small.

TestResult testGradedBedding() {
  // Test density-based settling: heavier elements sink below lighter ones.
  // Use sand (density=150) and ash (density=30) -- no reactions between them.
  // Drop both into water and check that sand settles lower.
  final e = _createEngine(30, 50);

  // Sealed container with water
  for (int x = 5; x <= 25; x++) {
    e.grid[5 * e.gridW + x] = El.stone;
    e.grid[45 * e.gridW + x] = El.stone;
  }
  for (int y = 5; y <= 45; y++) {
    e.grid[y * e.gridW + 5] = El.stone;
    e.grid[y * e.gridW + 25] = El.stone;
  }

  // Fill with water (y=6..44)
  for (int y = 10; y <= 44; y++) {
    for (int x = 6; x <= 24; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }

  // Drop mixed sand and stone from top (large density difference = 150 vs 255)
  for (int x = 8; x <= 22; x++) {
    e.grid[7 * e.gridW + x] = x % 2 == 0 ? El.sand : El.dirt;
    e.grid[8 * e.gridW + x] = x % 2 == 0 ? El.dirt : El.sand;
  }

  e.markAllDirty();
  _step(e, 400);

  // Sand (density=150) should settle deeper than dirt (density=145)
  // Both are granular and sink through water, but sand is denser
  double avgSandY = 0;
  double avgDirtY = 0;
  int sandCount = 0, dirtCount = 0;

  for (int y = 6; y <= 44; y++) {
    for (int x = 6; x <= 24; x++) {
      final el = e.grid[y * e.gridW + x];
      if (el == El.sand) {
        avgSandY += y;
        sandCount++;
      } else if (el == El.dirt) {
        avgDirtY += y;
        dirtCount++;
      }
    }
  }

  if (sandCount > 0) avgSandY /= sandCount;
  if (dirtCount > 0) avgDirtY /= dirtCount;

  // Sand should have higher Y (deeper = lower in screen = higher Y value)
  double score;
  if (sandCount == 0 && dirtCount == 0) {
    // Both may have reacted with water (sand->mud) -- partial credit
    score = 0.3;
  } else if (sandCount == 0 || dirtCount == 0) {
    score = 0.4; // One type missing
  } else if (avgSandY >= avgDirtY) {
    final diff = avgSandY - avgDirtY;
    score = diff > 0.5 ? 1.0 : 0.8;
  } else {
    final diff = avgDirtY - avgSandY;
    score = diff < 1.0 ? 0.5 : 0.2;
  }

  final oracle = _oracle('graded_bedding');
  if (oracle == null) return _oracleError('Graded Bedding', 'graded_bedding');

  return TestResult(
    'Graded Bedding',
    score,
    'sand: ${sandCount} cells avg Y=${avgSandY.toStringAsFixed(1)}, '
    'dirt: ${dirtCount} cells avg Y=${avgDirtY.toStringAsFixed(1)} '
    '(oracle: ${oracle['equation']}; sand vt=${oracle['stokes_vt_sand_m_s']} m/s)',
  );
}

// ===========================================================================
// 20. U-TUBE WITH DIFFERENT FLUIDS
// ===========================================================================
//
// Real physics: in a U-tube with water and oil, the oil column will be
// taller because oil is less dense. At equilibrium:
// h_oil / h_water = rho_water / rho_oil
//
// Our densities: water=100, oil=80 -> ratio = 100/80 = 1.25

TestResult testUTubeDifferentFluids() {
  final e = _createEngine(50, 50);

  // U-tube: left arm (x=5..15), right arm (x=30..40), connected at bottom
  // Left walls
  for (int y = 5; y <= 45; y++) {
    e.grid[y * e.gridW + 4] = El.stone;
    e.grid[y * e.gridW + 16] = El.stone;
  }
  // Right walls
  for (int y = 5; y <= 45; y++) {
    e.grid[y * e.gridW + 29] = El.stone;
    e.grid[y * e.gridW + 41] = El.stone;
  }
  // Bottom
  for (int x = 4; x <= 41; x++) {
    e.grid[45 * e.gridW + x] = El.stone;
  }
  // Separator walls with bottom gap
  for (int y = 5; y <= 38; y++) {
    e.grid[y * e.gridW + 16] = El.stone;
    e.grid[y * e.gridW + 29] = El.stone;
  }
  // Connect bottom channel (y=39..44)
  for (int y = 39; y <= 44; y++) {
    for (int x = 16; x <= 29; x++) {
      e.grid[y * e.gridW + x] = El.empty;
    }
  }

  // Fill left arm with water (y=15..44)
  for (int y = 15; y <= 44; y++) {
    for (int x = 5; x <= 15; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }
  // Fill right arm with oil (y=15..44)
  for (int y = 15; y <= 44; y++) {
    for (int x = 30; x <= 40; x++) {
      e.grid[y * e.gridW + x] = El.oil;
    }
  }
  // Also fill connector with water
  for (int y = 39; y <= 44; y++) {
    for (int x = 16; x <= 29; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }

  e.markAllDirty();
  _step(e, 500);

  // Measure surface levels
  int waterLevel = 45, oilLevel = 45;
  for (int y = 5; y < 45; y++) {
    for (int x = 5; x <= 15; x++) {
      if (e.grid[y * e.gridW + x] == El.water && y < waterLevel) {
        waterLevel = y;
      }
    }
    for (int x = 30; x <= 40; x++) {
      if (e.grid[y * e.gridW + x] == El.oil && y < oilLevel) {
        oilLevel = y;
      }
    }
  }

  final waterHeight = 45 - waterLevel;
  final oilHeight = 45 - oilLevel;

  // Oil column should be taller (lower level number = higher surface)
  // Use oracle expected ratio (required)
  final oracle = _oracle('u_tube_fluids');
  if (oracle == null) return _oracleError('U-Tube Fluids', 'u_tube_fluids');
  final expectedRatio = (oracle['our_expected_ratio'] as num).toDouble();

  double score;
  if (oilHeight > waterHeight) {
    final ratio = waterHeight > 0 ? oilHeight / waterHeight : 2.0;
    final error = (ratio - expectedRatio).abs() / expectedRatio;
    score = (1.0 - error * 0.5).clamp(0.3, 1.0);
  } else if (oilHeight == waterHeight) {
    score = 0.4;
  } else {
    score = 0.2;
  }

  return TestResult(
    'U-Tube Fluids',
    score,
    'water height=$waterHeight, oil height=$oilHeight '
    '(oracle: oil taller by factor ~${expectedRatio.toStringAsFixed(2)}, actual ratio='
    '${waterHeight > 0 ? (oilHeight / waterHeight).toStringAsFixed(2) : "inf"}; ${oracle['equation']})',
  );
}

// ===========================================================================
// 21. HYDROSTATIC PARADOX
// ===========================================================================
//
// Real physics: pressure at the bottom depends ONLY on liquid height,
// not on container shape. A narrow column and wide column of the same
// height should have the same bottom pressure.

TestResult testHydrostaticParadox() {
  // Both columns have the same height (30 cells of water) but different widths.
  // Our pressure system counts liquid depth per column, so it should give
  // the same result regardless of width.

  // Narrow column (3 cells wide), 30 cells tall
  int narrowPressure = 0;
  {
    final e = _createEngine(20, 50);
    for (int y = 10; y <= 45; y++) {
      e.grid[y * e.gridW + 8] = El.stone;
      e.grid[y * e.gridW + 12] = El.stone;
    }
    for (int x = 8; x <= 12; x++) {
      e.grid[45 * e.gridW + x] = El.stone;
    }
    // 30 cells of water: y=15..44
    for (int y = 15; y <= 44; y++) {
      for (int x = 9; x <= 11; x++) {
        e.grid[y * e.gridW + x] = El.water;
        e.life[y * e.gridW + x] = 100;
      }
    }
    e.markAllDirty();
    _step(e, 20);
    narrowPressure = e.pressure[44 * e.gridW + 10];
  }

  // Wide column (15 cells wide), same 30 cells tall
  int widePressure = 0;
  {
    final e = _createEngine(30, 50);
    for (int y = 10; y <= 45; y++) {
      e.grid[y * e.gridW + 3] = El.stone;
      e.grid[y * e.gridW + 20] = El.stone;
    }
    for (int x = 3; x <= 20; x++) {
      e.grid[45 * e.gridW + x] = El.stone;
    }
    // Same 30 cells of water: y=15..44
    for (int y = 15; y <= 44; y++) {
      for (int x = 4; x <= 19; x++) {
        e.grid[y * e.gridW + x] = El.water;
        e.life[y * e.gridW + x] = 100;
      }
    }
    e.markAllDirty();
    _step(e, 20);
    widePressure = e.pressure[44 * e.gridW + 10];
  }

  final oracle = _oracle('hydrostatic_paradox');
  if (oracle == null) return _oracleError('Hydrostatic Paradox', 'hydrostatic_paradox');

  // Both should have the same pressure (same height = 30 cells)
  final diff = (narrowPressure - widePressure).abs();
  final maxP = max(narrowPressure, widePressure);
  final score = maxP > 0 ? (1.0 - diff / maxP).clamp(0.0, 1.0) : 1.0;

  return TestResult(
    'Hydrostatic Paradox',
    score,
    'narrow pressure=$narrowPressure, wide=$widePressure, diff=$diff '
    '(oracle: expect diff=${oracle['expected_pressure_difference']}, '
    '${oracle['equation']})',
  );
}

// ===========================================================================
// 22. THERMAL EQUILIBRIUM
// ===========================================================================
//
// Real physics: hot object + cold object in an insulated box should reach
// thermal equilibrium at a temperature between the two starting values.

TestResult testThermalEquilibrium() {
  final e = _createEngine(30, 20);

  // Sealed stone box
  for (int x = 3; x <= 27; x++) {
    e.grid[3 * e.gridW + x] = El.stone;
    e.grid[17 * e.gridW + x] = El.stone;
  }
  for (int y = 3; y <= 17; y++) {
    e.grid[y * e.gridW + 3] = El.stone;
    e.grid[y * e.gridW + 27] = El.stone;
  }

  // Left half: hot stone (temp=220)
  for (int y = 4; y <= 16; y++) {
    for (int x = 4; x <= 14; x++) {
