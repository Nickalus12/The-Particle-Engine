/// Callback that dispatches element behavior (mirrors game's dispatch).
void _simulateElement(SimulationEngine e, int el, int x, int y, int idx) {
  // Check for custom behaviors first
  final custom = ElementRegistry.customBehaviors[el];
  if (custom != null) {
    custom(e, x, y, idx);
    return;
  }
  switch (el) {
    case El.sand:
      e.simSand(x, y, idx);
    case El.water:
      e.simWater(x, y, idx);
    case El.fire:
      e.simFire(x, y, idx);
    case El.ice:
      e.simIce(x, y, idx);
    case El.lightning:
      e.simLightning(x, y, idx);
    case El.seed:
      e.simSeed(x, y, idx);
    case El.stone:
      e.simStone(x, y, idx);
    case El.tnt:
      e.simTNT(x, y, idx);
    case El.rainbow:
      e.simRainbow(x, y, idx);
    case El.mud:
      e.simMud(x, y, idx);
    case El.steam:
      e.simSteam(x, y, idx);
    case El.oil:
      e.simOil(x, y, idx);
    case El.acid:
      e.simAcid(x, y, idx);
    case El.glass:
      e.simGlass(x, y, idx);
    case El.dirt:
      e.simDirt(x, y, idx);
    case El.plant:
      e.simPlant(x, y, idx);
    case El.lava:
      e.simLava(x, y, idx);
    case El.snow:
      e.simSnow(x, y, idx);
    case El.wood:
      e.simWood(x, y, idx);
    case El.metal:
      e.simMetal(x, y, idx);
    case El.smoke:
      e.simSmoke(x, y, idx);
    case El.bubble:
      e.simBubble(x, y, idx);
    case El.ash:
      e.simAsh(x, y, idx);
    case El.ant:
      e.simAnt(x, y, idx);
  }
}

SimulationEngine _createEngine(int w, int h) {
  ElementRegistry.init();
  ReactionRegistry.init();
  final engine = SimulationEngine(gridW: w, gridH: h);
  engine.markAllDirty();
  return engine;
}

void _step(SimulationEngine e, [int frames = 1]) {
  for (int i = 0; i < frames; i++) {
    e.markAllDirty();
    e.step(_simulateElement);
  }
}

// ---------------------------------------------------------------------------
// Test result data class
