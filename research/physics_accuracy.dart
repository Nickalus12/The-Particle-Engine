// ===========================================================================
// 13. MASS CONSERVATION
// ===========================================================================
//
// In a closed system with no reactions, total element cell count should
// remain constant. Elements should not spontaneously appear or disappear.

TestResult testMassConservation() {
  final e = _createEngine(60, 60);

  // Build a sealed box
  for (int x = 5; x <= 55; x++) {
    e.grid[5 * e.gridW + x] = El.stone;
    e.grid[55 * e.gridW + x] = El.stone;
  }
  for (int y = 5; y <= 55; y++) {
    e.grid[y * e.gridW + 5] = El.stone;
    e.grid[y * e.gridW + 55] = El.stone;
  }

  // Fill with a mix of elements that don't react with each other
  // Sand, dirt in the lower half; empty above for them to fall
  for (int y = 35; y <= 54; y++) {
    for (int x = 6; x <= 54; x++) {
      e.grid[y * e.gridW + x] = (x + y) % 2 == 0 ? El.sand : El.dirt;
    }
  }

  e.markAllDirty();

  // Count initial elements
  int initialCount = 0;
  for (int y = 6; y <= 54; y++) {
    for (int x = 6; x <= 54; x++) {
      if (e.grid[y * e.gridW + x] != El.empty &&
          e.grid[y * e.gridW + x] != El.stone) {
        initialCount++;
      }
    }
  }

  _step(e, 500);

  // Count final elements
  int finalCount = 0;
  for (int y = 6; y <= 54; y++) {
    for (int x = 6; x <= 54; x++) {
      if (e.grid[y * e.gridW + x] != El.empty &&
          e.grid[y * e.gridW + x] != El.stone) {
        finalCount++;
      }
    }
  }

  final oracle = _oracle('conservation_mass');
  if (oracle == null) return _oracleError('Mass Conservation', 'conservation_mass');

  final drift = (initialCount - finalCount).abs();
  final tolerancePct = (oracle['tolerance_percent'] as num).toDouble();
  final score =
      initialCount > 0 ? (1.0 - drift / initialCount).clamp(0.0, 1.0) : 1.0;

  return TestResult(
    'Mass Conservation',
    score,
    'before=$initialCount, after=$finalCount, drift=$drift cells '
    '(oracle: expect ${oracle['expected_drift']} drift, tolerance ${tolerancePct}%)',
  );
}

