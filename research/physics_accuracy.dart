// 26. EXPLOSION INVERSE SQUARE
// ===========================================================================
//
// Real physics: explosion energy decreases with distance^2 (inverse square).
// Test: detonate TNT and measure destruction at different radii.

TestResult testExplosionInverseSquare() {
  final e = _createEngine(80, 80);

  // Fill the grid with dirt (easily destroyed, good indicator)
  for (int y = 10; y <= 70; y++) {
    for (int x = 10; x <= 70; x++) {
      e.grid[y * e.gridW + x] = El.dirt;
    }
  }

  // Place single TNT at center
  const cx = 40;
  const cy = 40;
  e.grid[cy * e.gridW + cx] = El.tnt;
  // Ignite it
  e.grid[cy * e.gridW + (cx + 1)] = El.fire;
  e.markAllDirty();

  _step(e, 30);

  // Measure destruction at different radii
  final radiusDamage = <int, double>{};
  for (final radius in [3, 6, 9, 12, 15]) {
    int destroyed = 0;
    int total = 0;
    for (int dy = -radius; dy <= radius; dy++) {
      for (int dx = -radius; dx <= radius; dx++) {
        final d = sqrt(dx * dx + dy * dy);
        if (d >= radius - 1 && d <= radius + 1) {
          final ny = cy + dy;
          final nx = cx + dx;
          if (ny >= 10 && ny <= 70 && nx >= 10 && nx <= 70) {
            total++;
            if (e.grid[ny * e.gridW + nx] != El.dirt) destroyed++;
          }
        }
      }
    }
    radiusDamage[radius] = total > 0 ? destroyed / total : 0.0;
  }

  // Check that damage decreases with distance (inverse square law)
  final oracle = _oracle('explosion_falloff');
  if (oracle == null) return _oracleError('Explosion Falloff', 'explosion_falloff');
  int decreasingPairs = 0;
  int totalPairs = 0;
  final radii = radiusDamage.keys.toList()..sort();
  for (int i = 0; i < radii.length - 1; i++) {
    totalPairs++;
    if (radiusDamage[radii[i]]! >= radiusDamage[radii[i + 1]]! - 0.05) {
      decreasingPairs++;
    }
  }

  final score = totalPairs > 0 ? decreasingPairs / totalPairs : 0.0;

  final damageStr = radii
      .map((r) => 'r$r=${(radiusDamage[r]! * 100).toStringAsFixed(0)}%')
      .join(', ');

  return TestResult(
    'Explosion Falloff',
    score,
    '$damageStr '
    '(oracle: ${oracle['equation']})',
  );
}

