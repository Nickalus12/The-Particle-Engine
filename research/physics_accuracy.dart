// 12. CONNECTED VESSELS (Pascal's Principle)
// ===========================================================================
//
// Real physics: water in connected vessels seeks the same level.
// Test: create a U-tube with different water levels, check if they equalize.

TestResult testConnectedVessels() {
  final e = _createEngine(40, 40);

  // Build a U-tube: two vertical chambers connected at the bottom
  // Left chamber: x=5..10, y=5..35
  // Right chamber: x=20..25, y=5..35
  // Bottom connector: x=10..20, y=33..35

  // Left walls
  for (int y = 5; y <= 35; y++) {
    e.grid[y * e.gridW + 4] = El.stone;
    e.grid[y * e.gridW + 11] = El.stone;
  }
  // Right walls
  for (int y = 5; y <= 35; y++) {
    e.grid[y * e.gridW + 19] = El.stone;
    e.grid[y * e.gridW + 26] = El.stone;
  }
  // Bottom
  for (int x = 4; x <= 26; x++) {
    e.grid[35 * e.gridW + x] = El.stone;
  }
  // Separator (with gap at bottom)
  for (int y = 5; y <= 30; y++) {
    e.grid[y * e.gridW + 11] = El.stone;
    e.grid[y * e.gridW + 19] = El.stone;
  }
  // Connect bottom: clear the separator below y=30
  for (int y = 31; y <= 34; y++) {
    for (int x = 11; x <= 19; x++) {
      e.grid[y * e.gridW + x] = El.empty;
    }
  }

  // Fill left chamber with 25 rows of water (y=10..34)
  for (int y = 10; y <= 34; y++) {
    for (int x = 5; x <= 10; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }
  // Right chamber: only 5 rows of water (y=30..34)
  for (int y = 30; y <= 34; y++) {
    for (int x = 20; x <= 25; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }

  e.markAllDirty();
  _step(e, 500);

  // Measure water level in each chamber (topmost water cell)
  int leftLevel = 35, rightLevel = 35;
  for (int y = 5; y < 35; y++) {
    for (int x = 5; x <= 10; x++) {
      if (e.grid[y * e.gridW + x] == El.water && y < leftLevel) {
        leftLevel = y;
      }
    }
    for (int x = 20; x <= 25; x++) {
      if (e.grid[y * e.gridW + x] == El.water && y < rightLevel) {
        rightLevel = y;
      }
    }
  }

  final oracle = _oracle('connected_vessels');
  if (oracle == null) return _oracleError('Connected Vessels', 'connected_vessels');

  final levelDiff = (leftLevel - rightLevel).abs();
  final expectedDiff = (oracle['expected_level_difference'] as num).toInt();
  final tolerance = (oracle['tolerance_cells'] as num).toInt();
  // Perfect: levels equal (diff = 0). Allow ±3 cells for good score.
  final score = (1.0 - levelDiff / 15.0).clamp(0.0, 1.0);

  return TestResult(
    'Connected Vessels',
    score,
    'left level=$leftLevel, right level=$rightLevel, diff=$levelDiff cells '
    '(oracle: expect diff=$expectedDiff, tolerance=$tolerance cells; ${oracle['equation']})',
  );
}

