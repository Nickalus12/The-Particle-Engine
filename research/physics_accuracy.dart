    e.grid[45 * e.gridW + x] = El.stone;
  }
  for (int y = 15; y <= 44; y++) {
    for (int x = 5; x <= 10; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }

  // Short column (10 cells, x=18..23)
  for (int y = 5; y <= 45; y++) {
    e.grid[y * e.gridW + 17] = El.stone;
    e.grid[y * e.gridW + 24] = El.stone;
  }
  for (int x = 17; x <= 24; x++) {
    e.grid[45 * e.gridW + x] = El.stone;
  }
  for (int y = 35; y <= 44; y++) {
    for (int x = 18; x <= 23; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
    }
  }

  e.markAllDirty();
  _step(e, 20);

  final tallPressure = e.pressure[44 * e.gridW + 7];
  final shortPressure = e.pressure[44 * e.gridW + 20];

  final oracle = _oracle('load_distribution');
  if (oracle == null) return _oracleError('Load Distribution', 'load_distribution');

  final expectedPressureRatio = (oracle['expected_pressure_ratio'] as num).toDouble();

  // Tall column should have more pressure
  double score;
  if (tallPressure > shortPressure) {
    final ratio =
        shortPressure > 0 ? tallPressure / shortPressure : 10.0;
    final error = (ratio - expectedPressureRatio).abs() / expectedPressureRatio;
    score = (1.0 - error * 0.3).clamp(0.4, 1.0);
  } else if (tallPressure == shortPressure) {
    score = 0.3;
  } else {
    score = 0.1;
  }

  return TestResult(
    'Load Distribution',
    score,
    'tall column pressure=$tallPressure, short=$shortPressure '
    '(oracle: expect ratio ~${expectedPressureRatio.toStringAsFixed(1)}x; ${oracle['equation']})',
  );
}

