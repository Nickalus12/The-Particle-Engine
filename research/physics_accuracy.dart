TestResult testLoadDistribution() {
  final e = _createEngine(30, 50);

  // Two columns of water: one tall, one short. Both same width.
  // Measure bottom pressure of each.

  // Tall column (30 cells, x=5..10)
  for (int y = 5; y <= 45; y++) {
