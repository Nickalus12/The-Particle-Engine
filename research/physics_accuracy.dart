// ===========================================================================
// 10. NEWTON'S LAW OF COOLING
// ===========================================================================
//
// Real physics: dT/dt = -k * (T - T_ambient)
// Solution: T(t) = T_ambient + (T_0 - T_ambient) * e^(-k*t)
//
// Test: heat a stone, remove the heat source, measure the cooling curve.
// Score based on how well it fits an exponential decay toward ambient (128).

TestResult testNewtonCooling() {
  final e = _createEngine(20, 20);

  // Place a stone on a stone floor so it doesn't fall
  const x = 10;
  const y = 10;
  e.grid[y * e.gridW + x] = El.stone;
  e.temperature[y * e.gridW + x] = 250; // Very hot
  // Stone floor beneath to prevent falling
  for (int fx = 0; fx < 20; fx++) {
    e.grid[11 * e.gridW + fx] = El.stone;
  }

  e.markAllDirty();

  // Record temperature every 10 frames
  final temps = <double>[];
  final times = <double>[];

  for (int frame = 0; frame < 300; frame++) {
    if (frame % 10 == 0) {
      final idx = y * e.gridW + x;
      if (e.grid[idx] == El.stone) {
        temps.add(e.temperature[idx].toDouble());
        times.add(frame.toDouble());
      }
    }
    e.markAllDirty();
    e.step(_simulateElement);
  }

  if (temps.length < 3) {
    return TestResult(
        'Cooling Curve', 0.0, 'Not enough data points (stone disappeared)');
  }

  // Check exponential decay properties:
  // 1. Temperature should monotonically decrease
  // 2. Rate of decrease should slow down (exponential, not linear)
  // 3. Should approach ambient (128)

  int monotonicCount = 0;
  int deceleratingCount = 0;
  for (int i = 0; i < temps.length - 1; i++) {
    if (temps[i] >= temps[i + 1]) monotonicCount++;
    if (i < temps.length - 2) {
      final rate1 = temps[i] - temps[i + 1];
      final rate2 = temps[i + 1] - temps[i + 2];
      if (rate1 >= rate2 - 0.5) deceleratingCount++; // allow small noise
    }
  }

  final monotonicScore =
      (temps.length > 1) ? monotonicCount / (temps.length - 1) : 0.0;
  final decelScore =
      (temps.length > 2) ? deceleratingCount / (temps.length - 2) : 0.0;

  // Check that it moved toward ambient
  final initialTemp = temps.first;
  final finalTemp = temps.last;
  final movedTowardAmbient =
      (finalTemp - 128).abs() < (initialTemp - 128).abs();

  double score = (monotonicScore * 0.4 + decelScore * 0.4) +
      (movedTowardAmbient ? 0.2 : 0.0);

  // Compare against oracle analytical curve (required)
  final oracle = _oracle('cooling_curve');
  if (oracle == null) return _oracleError('Cooling Curve', 'cooling_curve');

  String oracleNote = '';
  if (temps.length >= 3) {
    final analyticalTemps = (oracle['analytical_temps'] as List);
    final oracleFrames = (oracle['frames'] as List);
    // Compare our measured temps against oracle at matching frame indices
    double totalError = 0;
    int matchCount = 0;
    for (int i = 0; i < times.length && i < oracleFrames.length; i++) {
      final frame = times[i].round();
      // Find matching frame in oracle
      for (int j = 0; j < oracleFrames.length; j++) {
        if ((oracleFrames[j] as num).toInt() == frame) {
          final expected = (analyticalTemps[j] as num).toDouble();
          final actual = temps[i];
          totalError += (actual - expected).abs();
          matchCount++;
          break;
        }
      }
    }
    if (matchCount > 0) {
      final avgError = totalError / matchCount;
      final curveAccuracy = (1.0 - avgError / 122.0).clamp(0.0, 1.0);
      oracleNote = ' | oracle curve fit: ${(curveAccuracy * 100).toStringAsFixed(0)}% '
          '(avg err=${avgError.toStringAsFixed(1)})';
    }
  }

  return TestResult(
    'Cooling Curve',
    score,
    'T: ${initialTemp.toStringAsFixed(0)} -> ${finalTemp.toStringAsFixed(0)} '
    '(ambient=128), monotonic: ${(monotonicScore * 100).toStringAsFixed(0)}%, '
    'decelerating: ${(decelScore * 100).toStringAsFixed(0)}% '
    '(oracle: ${oracle['equation']})$oracleNote',
  );
}

