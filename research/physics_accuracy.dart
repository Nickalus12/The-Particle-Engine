// ===========================================================================
// 23. THERMAL STRATIFICATION
// ===========================================================================
//
// Real physics: in a deep water body, hot water layers on top of cold water
// (thermal stratification). Our engine implements convection for this.

TestResult testThermalStratification() {
  final oracleCheck = _oracle('thermal_stratification');
  if (oracleCheck == null) return _oracleError('Thermal Stratification', 'thermal_stratification');

  final e = _createEngine(20, 40);

  // Sealed container -- use metal walls (won't react with water)
  for (int x = 3; x <= 17; x++) {
    e.grid[3 * e.gridW + x] = El.metal;
    e.grid[35 * e.gridW + x] = El.metal;
  }
  for (int y = 3; y <= 35; y++) {
    e.grid[y * e.gridW + 3] = El.metal;
    e.grid[y * e.gridW + 17] = El.metal;
  }

  // Fill with water -- hot at bottom, cold at top (inverted stratification)
  // After simulation, convection should fix this: hot rises, cold sinks
  for (int y = 4; y <= 34; y++) {
    for (int x = 4; x <= 16; x++) {
      e.grid[y * e.gridW + x] = El.water;
      e.life[y * e.gridW + x] = 100;
      // Top half cold (y=4..19), bottom half hot (y=20..34)
      e.temperature[y * e.gridW + x] = (y < 19) ? 160 : 180;
    }
  }

  e.markAllDirty();
  _step(e, 300);

  // Measure: top should be warmer or equal to bottom after convection
  // Sample 5 layers
  final layerTemps = <double>[];
  for (int y = 5; y <= 33; y += 3) {
    double sum = 0;
    int cnt = 0;
    for (int x = 5; x <= 15; x++) {
      final idx = y * e.gridW + x;
      if (e.grid[idx] == El.water) {
        sum += e.temperature[idx];
        cnt++;
      }
    }
    if (cnt > 0) layerTemps.add(sum / cnt);
  }

  if (layerTemps.length < 2) {
    // Water may have evaporated due to high temps -- check temperature of
    // whatever is there
    double topTemp = 0, botTemp = 0;
    int topCnt = 0, botCnt = 0;
    for (int x = 5; x <= 15; x++) {
      topTemp += e.temperature[6 * e.gridW + x];
      topCnt++;
      botTemp += e.temperature[32 * e.gridW + x];
      botCnt++;
    }
    topTemp = topCnt > 0 ? topTemp / topCnt : 128;
    botTemp = botCnt > 0 ? botTemp / botCnt : 128;
    final topWarmer = topTemp >= botTemp - 3;
    return TestResult(
      'Thermal Stratification',
      topWarmer ? 0.6 : 0.2,
      'water evaporated, checking raw temps: '
      'top=${topTemp.toStringAsFixed(0)}, bot=${botTemp.toStringAsFixed(0)}',
    );
  }

  // Count how many adjacent layers have correct ordering (top warmer)
  int correctPairs = 0;
  final totalPairs = layerTemps.length - 1;
  for (int i = 0; i < totalPairs; i++) {
    if (layerTemps[i] >= layerTemps[i + 1] - 3) {
      correctPairs++;
    }
  }

  final score = totalPairs > 0 ? correctPairs / totalPairs : 0.0;

  return TestResult(
    'Thermal Stratification',
    score,
    '$correctPairs/$totalPairs layer pairs correctly stratified '
    '(oracle: ${oracleCheck['mechanism']})',
  );
}

// ===========================================================================
// 24. FIRE SPREAD RATE
// ===========================================================================
//
// Real physics: fire spreading through a uniform fuel line should have
// roughly constant velocity (after initial ignition phase).

TestResult testFireSpreadRate() {
  final e = _createEngine(100, 20);

  // Line of wood on a stone floor with stone walls above to contain
  for (int x = 0; x < 100; x++) {
    e.grid[16 * e.gridW + x] = El.stone; // floor
    e.grid[12 * e.gridW + x] = El.stone; // ceiling (contain fire/smoke)
  }
  // 2-row thick wood line for more fuel
  for (int x = 5; x <= 95; x++) {
    e.grid[14 * e.gridW + x] = El.wood;
    e.grid[15 * e.gridW + x] = El.wood;
  }
  // Ignite at left end (replace wood with fire)
  e.grid[14 * e.gridW + 5] = El.fire;
  e.grid[15 * e.gridW + 5] = El.fire;
  e.markAllDirty();

  // Record fire/burn front position over time
  final frontPositions = <int>[];
  for (int frame = 0; frame < 600; frame++) {
    e.markAllDirty();
    e.step(_simulateElement);

    if (frame % 30 == 0) {
      // Find rightmost non-wood cell in the wood line (the burn front)
      int front = 5;
      for (int x = 5; x <= 95; x++) {
        final cell14 = e.grid[14 * e.gridW + x];
        final cell15 = e.grid[15 * e.gridW + x];
        if (cell14 != El.wood || cell15 != El.wood) {
          front = x;
        }
      }
      frontPositions.add(front);
    }
  }

  final oracle = _oracle('fire_spread');
  if (oracle == null) return _oracleError('Fire Spread Rate', 'fire_spread');

  // Check for roughly constant velocity (after initial phase)
  if (frontPositions.length < 6) {
    return TestResult('Fire Spread Rate', 0.0, 'not enough data');
  }

  // Skip first 4 measurements (ignition phase)
  final velocities = <double>[];
  for (int i = 4; i < frontPositions.length - 1; i++) {
    velocities.add((frontPositions[i + 1] - frontPositions[i]).toDouble());
  }

  if (velocities.isEmpty || velocities.every((v) => v == 0)) {
    // Fire didn't spread far -- check if it spread at all
    final totalSpread = frontPositions.last - frontPositions.first;
    if (totalSpread > 5) {
      return TestResult(
          'Fire Spread Rate', 0.5, 'fire spread $totalSpread cells but erratically');
    }
    return TestResult(
        'Fire Spread Rate', 0.3, 'fire barely spread ($totalSpread cells)');
  }

  // Coefficient of variation: std/mean (lower = more constant)
  final mean = velocities.reduce((a, b) => a + b) / velocities.length;
  double stdDev = 0;
  for (final v in velocities) {
    stdDev += (v - mean) * (v - mean);
  }
  stdDev = sqrt(stdDev / velocities.length);
  final cv = mean > 0 ? stdDev / mean : 1.0;

  // CV < 0.5 is quite constant, > 1.5 is very erratic
  final expectedCvBelow = (oracle['expected_cv_below'] as num).toDouble();
  final score = (1.0 - cv * 0.4).clamp(0.0, 1.0);

  return TestResult(
    'Fire Spread Rate',
    score,
    'velocity CV=${cv.toStringAsFixed(2)} '
    '(mean=${mean.toStringAsFixed(1)} cells/30frames, '
    'front: ${frontPositions.first}->${frontPositions.last}) '
    '(oracle: expect CV < $expectedCvBelow; ${oracle['equation']})',
  );
}

// ===========================================================================
// 25. FLASH POINT ORDERING
// ===========================================================================
//
// Real physics: different materials have different ignition thresholds.
// Oil should ignite more easily (lower flash point) than wood.
// Our engine: oil probability=0.5 per tick with fire neighbor,
//             wood chars gradually (probability=0.15).

TestResult testFlashPointOrdering() {
  // Measure how many frames until element catches fire
  int oilIgnitionTime = _measureIgnitionTime(El.oil, 200);
  int woodIgnitionTime = _measureIgnitionTime(El.wood, 200);

  final oracle = _oracle('flash_point');
  if (oracle == null) return _oracleError('Flash Point Order', 'flash_point');

  // Oil should ignite faster than wood
  double score;
  if (oilIgnitionTime < woodIgnitionTime && oilIgnitionTime > 0) {
    score = 1.0;
  } else if (oilIgnitionTime == woodIgnitionTime) {
    score = 0.5;
  } else if (oilIgnitionTime > 0 && woodIgnitionTime > 0) {
    score = 0.2; // Wrong ordering
  } else {
    score = 0.3; // One didn't ignite
  }

  final rateRatio = (oracle['rate_ratio_oil_to_wood'] as num).toDouble();
  return TestResult(
    'Flash Point Order',
    score,
    'oil ignited at frame $oilIgnitionTime, '
    'wood at frame $woodIgnitionTime '
    '(oracle: Arrhenius rate ratio oil/wood = ${rateRatio.toStringAsFixed(1)}x; ${oracle['equation']})',
  );
}

int _measureIgnitionTime(int element, int maxFrames) {
  // Run multiple trials and average, since reactions are probabilistic
  int totalFrames = 0;
  const trials = 5;
  for (int trial = 0; trial < trials; trial++) {
    final e = _createEngine(10, 10);
    // Place element on stone floor with fire adjacent
    for (int x = 0; x < 10; x++) {
      e.grid[6 * e.gridW + x] = El.stone;
    }
    e.grid[5 * e.gridW + 5] = element;
    if (element == El.water || element == El.oil) {
      e.life[5 * e.gridW + 5] = 100;
    }
    // Fire adjacent but not overlapping
    e.grid[5 * e.gridW + 4] = El.fire;
    e.markAllDirty();

    int ignitionFrame = maxFrames;
    for (int frame = 1; frame <= maxFrames; frame++) {
      e.markAllDirty();
      e.step(_simulateElement);
      final cell = e.grid[5 * e.gridW + 5];
      if (cell != element) {
        ignitionFrame = frame;
        break;
      }
    }
    totalFrames += ignitionFrame;
  }
  return totalFrames ~/ trials;
}

