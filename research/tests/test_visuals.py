"""
Visual Quality Test Suite
==========================

Uses scikit-image, colour-science, and numpy to validate rendering quality
of The Particle Engine. All thresholds are read from visual_ground_truth.json
generated by visual_oracle.py.

Run:
    python research/visual_oracle.py
    pytest research/tests/test_visuals.py -v
"""

import json
import os

import colour
import numpy as np
import pytest
from skimage.color import rgb2lab
from skimage.filters import sobel
from skimage.measure import shannon_entropy

RESEARCH_DIR = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))

# ============================================================================
# Helpers
# ============================================================================


def _lab_from_rgb(r, g, b):
    """Convert 0-255 RGB to CIE LAB."""
    rgb = np.array([[[r / 255.0, g / 255.0, b / 255.0]]])
    return rgb2lab(rgb)[0, 0]


def _delta_e(lab1, lab2):
    """CIE 2000 Delta E between two LAB arrays."""
    return float(colour.delta_E(np.array(lab1), np.array(lab2), method="CIE 2000"))


def _luminance_from_rgb(r, g, b):
    """Relative luminance per WCAG 2.0 (sRGB)."""

    def _lin(c):
        c = c / 255.0
        return c / 12.92 if c <= 0.03928 else ((c + 0.055) / 1.055) ** 2.4

    return 0.2126 * _lin(r) + 0.7152 * _lin(g) + 0.0722 * _lin(b)


def _contrast_ratio(rgb1, rgb2):
    """WCAG contrast ratio between two RGB tuples."""
    l1 = _luminance_from_rgb(*rgb1) + 0.05
    l2 = _luminance_from_rgb(*rgb2) + 0.05
    return max(l1, l2) / min(l1, l2)


def _load_ground_truth():
    """Load ground truth for parametrize decorators (outside fixtures)."""
    path = os.path.join(RESEARCH_DIR, "visual_ground_truth.json")
    if not os.path.exists(path):
        return {}
    with open(path) as f:
        return json.load(f)


_GT = _load_ground_truth()


