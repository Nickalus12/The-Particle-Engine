def test_edge_quality_synthetic(ground_truth):
    """Sobel edge magnitudes on a clean element boundary should be bounded.

    Creates a synthetic two-region image and verifies edge detection works.
    """
    img = np.zeros((32, 32), dtype=float)
    img[:, 16:] = 1.0
    edges = sobel(img)
    max_edge = float(np.max(edges))
    assert 0.0 < max_edge <= 1.0, (
        f"Edge magnitude out of range: {max_edge:.4f}"
    )
    mean_edge = float(np.mean(edges))
    assert mean_edge < 0.2, f"Too many edges: mean = {mean_edge:.4f}"


