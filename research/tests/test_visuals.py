def _elements_with_entropy():
    """Return elements that have expected entropy ranges."""
    return list(_GT.get("texture_entropy", {}).items())


@pytest.mark.parametrize(
    "element, entropy_range",
    _elements_with_entropy(),
    ids=[name for name, _ in _elements_with_entropy()],
)
def test_element_entropy_range_defined(ground_truth, element, entropy_range):
    """Each element with entropy expectations should have valid min < max."""
    assert entropy_range["min"] < entropy_range["max"], (
        f"{element}: invalid entropy range "
        f"[{entropy_range['min']}, {entropy_range['max']}]"
    )


@pytest.mark.parametrize(
    "element, entropy_range",
    _elements_with_entropy(),
    ids=[f"{name}_synthetic" for name, _ in _elements_with_entropy()],
)
def test_texture_variation_synthetic(ground_truth, element, entropy_range):
    """Synthetic texture patches should have non-zero entropy (not flat).

    Generates a small test patch with the element's base color + noise
    and verifies Shannon entropy is within the expected range.
    """
    base_rgb = ground_truth["base_rgb"].get(element)
    if base_rgb is None:
        pytest.skip(f"No base RGB for {element}")

    rng = np.random.RandomState(42)
    patch = np.zeros((16, 16), dtype=np.uint8)
    gray_base = int(0.299 * base_rgb["r"] + 0.587 * base_rgb["g"] + 0.114 * base_rgb["b"])
    noise_level = max(5, int(entropy_range["min"] * 8))
    patch[:] = np.clip(
        gray_base + rng.randint(-noise_level, noise_level + 1, (16, 16)),
        0,
        255,
    )
    entropy = shannon_entropy(patch)
    assert entropy > 0.0, f"{element}: flat texture (zero entropy)"


def test_no_banding_in_smooth_gradient(ground_truth):
    """A smooth gradient should not exhibit banding (high local entropy)."""
    gradient = np.linspace(50, 200, 256, dtype=np.uint8).reshape(1, 256)
    gradient_2d = np.tile(gradient, (16, 1))
    edges = sobel(gradient_2d.astype(float) / 255.0)
    max_edge = float(np.max(edges))
    assert max_edge < 0.15, f"Banding detected: max Sobel edge = {max_edge:.4f}"


