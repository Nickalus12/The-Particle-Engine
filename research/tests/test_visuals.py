def test_glow_falloff(ground_truth):
    """Light emission should decrease with distance using quadratic falloff.

    Simulates the falloff function 1 - (d/r)^2 and verifies monotonic decrease.
    """
    glow = ground_truth["glow"]
    max_r = glow["max_radius"]

    distances = np.linspace(0, max_r, 20)
    intensities = np.clip(1.0 - (distances / max_r) ** 2, 0, 1)

    for i in range(1, len(intensities)):
        assert intensities[i] <= intensities[i - 1] + 1e-9, (
            f"Glow increased at d={distances[i]:.2f}: "
            f"{intensities[i]:.4f} > {intensities[i-1]:.4f}"
        )

    assert intensities[0] == pytest.approx(1.0), "Glow at center should be 1.0"
    assert intensities[-1] == pytest.approx(0.0, abs=1e-6), (
        f"Glow at max radius should be ~0, got {intensities[-1]:.4f}"
    )
    assert glow["no_black_halos"] is True, "Black halos should be disabled"


def test_temperature_tinting(ground_truth):
    """Hot elements should have warm LAB a* values, cold should have cool."""
    lab_colors = ground_truth["element_lab_colors"]

    hot_elements = ["fire", "lava"]
    cold_elements = ["ice", "snow"]

    for el in hot_elements:
        if el in lab_colors:
            lab = lab_colors[el]
            assert lab["a"] > 0 or lab["b"] > 0, (
                f"Hot element {el} should have warm tint "
                f"(a*={lab['a']:.1f}, b*={lab['b']:.1f})"
            )

    for el in cold_elements:
        if el in lab_colors:
            lab = lab_colors[el]
            assert lab["b"] < 15, (
                f"Cold element {el} should have cool tint (b*={lab['b']:.1f})"
            )


