@pytest.mark.parametrize(
    "pair",
    _critical_pairs(),
    ids=[f"{p[0]}_vs_{p[1]}_contrast" for p in _critical_pairs()],
)
def test_contrast_ratio(ground_truth, pair):
    """Critical element pairs should meet minimum contrast ratio."""
    el1, el2 = pair
    rgb1 = ground_truth["base_rgb"][el1]
    rgb2 = ground_truth["base_rgb"][el2]
    cr = _contrast_ratio(
        (rgb1["r"], rgb1["g"], rgb1["b"]),
        (rgb2["r"], rgb2["g"], rgb2["b"]),
    )
    min_cr = ground_truth["contrast"]["min_contrast_ratio"]
    assert cr >= min_cr - 0.1, (
        f"{el1} vs {el2}: contrast ratio {cr:.2f} below {min_cr}"
    )


