def test_ground_truth_has_all_categories(ground_truth):
    """Ground truth should contain all expected top-level categories."""
    expected = {
        "element_lab_colors",
        "delta_e_pairs",
        "min_delta_e_threshold",
        "texture_entropy",
        "sky_gradient",
        "underground",
        "transparency",
        "glow",
        "water_depth",
        "contrast",
        "light_emission",
        "base_alphas",
        "base_rgb",
    }
    missing = expected - set(ground_truth.keys())
    assert not missing, f"Ground truth missing categories: {missing}"


def test_lab_values_in_valid_range(ground_truth):
    """All LAB values should be in valid CIE LAB range."""
    for element, lab in ground_truth["element_lab_colors"].items():
        assert 0 <= lab["L"] <= 100, f"{element}: L*={lab['L']} out of [0,100]"
        assert -128 <= lab["a"] <= 127, f"{element}: a*={lab['a']} out of [-128,127]"
        assert -128 <= lab["b"] <= 127, f"{element}: b*={lab['b']} out of [-128,127]"


def test_delta_e_symmetry(ground_truth):
    """Delta E should cover all pairs from LAB colors (completeness check)."""
    lab_colors = ground_truth["element_lab_colors"]
    names = list(lab_colors.keys())
    expected_pairs = 0
    for i in range(len(names)):
        for j in range(i + 1, len(names)):
            expected_pairs += 1
    actual_pairs = len(ground_truth["delta_e_pairs"])
    assert actual_pairs == expected_pairs, (
        f"Expected {expected_pairs} pairs, got {actual_pairs}"
    )
