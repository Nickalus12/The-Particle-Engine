def test_sky_gradient_smoothness(ground_truth):
    """Sky gradient second derivative should be below threshold.

    Simulates a vertical sky gradient in LAB lightness and checks
    that the second derivative (acceleration) stays smooth.
    """
    sky = ground_truth["sky_gradient"]
    top_l = np.mean(sky["top_L_range"])
    bot_l = np.mean(sky["bottom_L_range"])

    n_rows = 100
    lightness = np.linspace(top_l, bot_l, n_rows)
    second_deriv = np.diff(lightness, n=2)
    max_sd = float(np.max(np.abs(second_deriv)))
    threshold = sky["max_second_derivative"]
    assert max_sd < threshold + 1.0, (
        f"Sky gradient too abrupt: max 2nd derivative = {max_sd:.2f}"
    )


def test_water_depth_gradient(ground_truth):
    """Deeper water should be darker than surface water."""
    wd = ground_truth["water_depth"]
    assert wd["surface_brightness_min"] > wd["deep_brightness_max"], (
        f"Water depth gradient inverted: surface min {wd['surface_brightness_min']} "
        f"<= deep max {wd['deep_brightness_max']}"
    )
    assert wd["gradient_direction"] == "darker_with_depth"


def test_underground_darkness(ground_truth):
    """Underground regions should have constrained brightness."""
    ug = ground_truth["underground"]
    assert ug["max_avg_brightness"] <= 120, (
        f"Underground too bright: max avg = {ug['max_avg_brightness']}"
    )
    assert ug["max_brightness_std"] <= 40, (
        f"Underground too varied: max std = {ug['max_brightness_std']}"
    )


def test_day_night_brightness(ground_truth):
    """Night sky should be measurably darker than day sky."""
    sky = ground_truth["sky_gradient"]
    day_top = np.mean(sky["top_L_range"])
    night_top = np.mean(sky["night_top_L_range"])
    assert night_top < day_top, (
        f"Night ({night_top:.1f}) not darker than day ({day_top:.1f})"
    )
    assert day_top - night_top > 30, (
        f"Day/night difference too small: {day_top - night_top:.1f}"
    )


