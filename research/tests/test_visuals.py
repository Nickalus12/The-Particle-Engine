# Color Tests (CIE 2000 Delta E)
# ============================================================================


def _all_element_pairs():
    """Generate all unique element pairs from ground truth."""
    pairs = _GT.get("delta_e_pairs", {})
    result = []
    for key, de_val in pairs.items():
        parts = key.split("_")
        if len(parts) == 2:
            result.append((parts[0], parts[1], de_val))
    return result


def _critical_pairs():
    """Return critical pairs that must be visually distinguishable."""
    contrast = _GT.get("contrast", {})
    return contrast.get("critical_pairs", [])


def _all_elements_with_lab():
    """Return all elements with their LAB colors."""
    return list(_GT.get("element_lab_colors", {}).items())


@pytest.mark.parametrize(
    "el1, el2, expected_de",
    _all_element_pairs(),
    ids=[k for k in _GT.get("delta_e_pairs", {}).keys()],
)
def test_all_pairs_delta_e(ground_truth, el1, el2, expected_de):
    """Every element pair should have Delta E > threshold for distinguishability."""
    known_similar = ground_truth.get("known_similar_pairs", [])
    pair_key = f"{el1}_{el2}"
    reverse_key = f"{el2}_{el1}"
    if pair_key in known_similar or reverse_key in known_similar:
        pytest.skip(f"{el1}/{el2} are known similar elements (by design)")
    threshold = ground_truth["min_delta_e_threshold"]
    assert expected_de >= threshold - 0.5, (
        f"{el1} vs {el2}: Delta E {expected_de:.2f} below threshold {threshold}"
    )


@pytest.mark.parametrize(
    "pair",
    _critical_pairs(),
    ids=[f"{p[0]}_vs_{p[1]}" for p in _critical_pairs()],
)
def test_critical_pairs(ground_truth, pair):
    """Critical element pairs must be clearly distinguishable (Delta E > threshold)."""
    el1, el2 = pair
    lab1_data = ground_truth["element_lab_colors"][el1]
    lab2_data = ground_truth["element_lab_colors"][el2]
    lab1 = [lab1_data["L"], lab1_data["a"], lab1_data["b"]]
    lab2 = [lab2_data["L"], lab2_data["a"], lab2_data["b"]]
    de = _delta_e(lab1, lab2)
    threshold = ground_truth["min_delta_e_threshold"]
    assert de >= threshold - 0.5, (
        f"{el1} vs {el2}: Delta E {de:.2f} is too low (need > {threshold})"
    )


@pytest.mark.parametrize(
    "element, lab_data",
    _all_elements_with_lab(),
    ids=[name for name, _ in _all_elements_with_lab()],
)
def test_element_color_in_range(ground_truth, element, lab_data):
    """Each element's base color should produce a valid LAB value.

    The rendered color should stay within Delta E < 30 of the base color.
    This tests the oracle's LAB conversion is self-consistent.
    """
    base_rgb = ground_truth["base_rgb"].get(element)
    if base_rgb is None:
        pytest.skip(f"No base RGB for {element}")
    computed_lab = _lab_from_rgb(base_rgb["r"], base_rgb["g"], base_rgb["b"])
    expected_lab = [lab_data["L"], lab_data["a"], lab_data["b"]]
    de = _delta_e(computed_lab, expected_lab)
    assert de < 30.0, (
        f"{element}: recomputed LAB diverges from oracle (Delta E = {de:.2f})"
    )


@pytest.mark.parametrize(
    "element, lab_data",
    _all_elements_with_lab(),
    ids=[f"{name}_consistency" for name, _ in _all_elements_with_lab()],
)
def test_no_color_drift(ground_truth, element, lab_data):
    """Same element computed twice should give identical LAB (no floating point drift)."""
    base_rgb = ground_truth["base_rgb"].get(element)
    if base_rgb is None:
        pytest.skip(f"No base RGB for {element}")
    lab_a = _lab_from_rgb(base_rgb["r"], base_rgb["g"], base_rgb["b"])
    lab_b = _lab_from_rgb(base_rgb["r"], base_rgb["g"], base_rgb["b"])
    de = _delta_e(lab_a, lab_b)
    assert de == pytest.approx(
        0.0, abs=1e-6
    ), f"{element}: color drift detected (Delta E = {de:.6f})"


